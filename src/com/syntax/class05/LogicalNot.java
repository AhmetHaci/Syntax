package com.syntax.class05;

public class LogicalNot {

	public static void main(String[] args) {
		boolean b = !true;
		boolean val = !false;
		System.out.println(b);
		System.out.println(val);
	
	System.out.println("+++++++++++++++++++++++++++++++");
	
	boolean isCold=!true;
	if (!isCold) {
		System.out.println("Hello");
	}else {
		System.out.println("Bye");
	}
	
	
	}

}
