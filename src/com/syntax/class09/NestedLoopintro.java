package com.syntax.class09;

public class NestedLoopintro {

	public static void main(String[] args) {

		for (int i = 1; i <= 3; i++) {
			//System.out.println("I m outer the loop");
			for (int j = 1; j <= 3; j++) {
				//System.out.println("I am an innner loop");
			System.out.println(i+" "+ j);
			}
		}

	}

}
